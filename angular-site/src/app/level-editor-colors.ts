/** Convert an 8-bit Mac palette index to RGBA8888. */
export function indexed8ToRgba(value: number): [number, number, number, number] {
  const entry = MAC_SYSTEM_PALETTE[value & 0xff];
  if (!entry) return [0, 0, 0, 255];
  return [entry[0], entry[1], entry[2], 255];
}

let _paletteCache: Map<number, number> | null = null;

/** Convert RGBA8888 to the nearest Mac 8-bit palette index using a cached LUT. */
export function rgbaToMacPaletteIndex(r: number, g: number, b: number): number {
  if (!_paletteCache) {
    const cache = new Map<number, number>();
    for (let rq = 0; rq < 32; rq++) {
      for (let gq = 0; gq < 32; gq++) {
        for (let bq = 0; bq < 32; bq++) {
          const key = (rq << 10) | (gq << 5) | bq;
          const rr = rq * 8, gg = gq * 8, bb = bq * 8;
          let bestIdx = 0, bestDist = Infinity;
          for (let i = 0; i < MAC_SYSTEM_PALETTE.length; i++) {
            const [pr, pg, pb] = MAC_SYSTEM_PALETTE[i] ?? [0, 0, 0];
            const dist = (rr - pr) ** 2 + (gg - pg) ** 2 + (bb - pb) ** 2;
            if (dist < bestDist) { bestDist = dist; bestIdx = i; }
          }
          cache.set(key, bestIdx);
        }
      }
    }
    _paletteCache = cache;
  }
  return _paletteCache.get(((r >> 3) << 10) | ((g >> 3) << 5) | (b >> 3)) ?? 0;
}
